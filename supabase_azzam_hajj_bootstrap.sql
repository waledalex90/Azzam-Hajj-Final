create or replace function app.submit_attendance_checks(
  p_round_id bigint,
  p_payload jsonb
)
returns table(inserted_count integer, updated_count integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id bigint;
  v_role text;
  v_site_id bigint;
  i_count integer := 0;
  u_count integer := 0;
begin
  v_user_id := app.current_user_id();
  v_role := app.current_user_role();
  if v_user_id is null then
    raise exception 'Unauthorized user';
  end if;
  if v_role not in ('admin', 'hr', 'technical_observer', 'field_observer')
     and not app.has_granular_permission('edit_attendance') then
    raise exception 'Only admin/hr/technical_observer/field_observer can submit checks';
