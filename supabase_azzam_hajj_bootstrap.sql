-- =========================
-- Daily summary refresh
-- =========================
create or replace function app.refresh_attendance_daily_summary(p_worker_id bigint, p_work_date date)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_site_id bigint;
  v_total integer;
  v_present numeric(8,2);
  v_absent numeric(8,2);
  v_half numeric(8,2);
  v_final public.attendance_status;
  v_last_confirmed timestamptz;
begin
  select
    max(ar.site_id),
    count(*)::integer,
    coalesce(sum(case when ac.status = 'present' then 1 else 0 end), 0),
    coalesce(sum(case when ac.status = 'absent' then 1 else 0 end), 0),
    coalesce(sum(case when ac.status = 'half' then 1 else 0 end), 0),
    max(ac.confirmed_at)
  into v_site_id, v_total, v_present, v_absent, v_half, v_last_confirmed
  from public.attendance_checks ac
  join public.attendance_rounds ar on ar.id = ac.round_id
  where ac.worker_id = p_worker_id
    and ar.work_date = p_work_date
    and ac.confirmation_status = 'confirmed';

  if coalesce(v_total, 0) = 0 then
    delete from public.attendance_daily_summary
    where worker_id = p_worker_id and work_date = p_work_date;
    return;
  end if;

  if v_present >= greatest(v_absent, v_half) then
    v_final := 'present';
  elsif v_absent >= greatest(v_present, v_half) then
    v_final := 'absent';
  else
    v_final := 'half';
  end if;

  insert into public.attendance_daily_summary(
    worker_id, work_date, site_id, total_rounds,
    present_points, absent_points, half_points, final_status, last_confirmed_at
  )
  values (
    p_worker_id, p_work_date, v_site_id, v_total,
    v_present, v_absent, v_half, v_final, v_last_confirmed
  )
  on conflict (worker_id, work_date) do update
    set site_id = excluded.site_id,
        total_rounds = excluded.total_rounds,
        present_points = excluded.present_points,
        absent_points = excluded.absent_points,
        half_points = excluded.half_points,
        final_status = excluded.final_status,
        last_confirmed_at = excluded.last_confirmed_at,
        updated_at = now();
end;
$$;

create or replace function app.trg_refresh_attendance_daily_summary()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_worker_id bigint;
  v_work_date date;
begin
  if tg_op = 'DELETE' then
    v_worker_id := old.worker_id;
    select work_date into v_work_date from public.attendance_rounds where id = old.round_id;
  else
    v_worker_id := new.worker_id;
    select work_date into v_work_date from public.attendance_rounds where id = new.round_id;
  end if;

  if v_work_date is not null then
    perform app.refresh_attendance_daily_summary(v_worker_id, v_work_date);
  end if;

  return coalesce(new, old);
end;
$$;

drop trigger if exists trg_refresh_attendance_daily_summary on public.attendance_checks;
create trigger trg_refresh_attendance_daily_summary
after insert or update or delete on public.attendance_checks
for each row execute function app.trg_refresh_attendance_daily_summary();

