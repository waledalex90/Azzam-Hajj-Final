returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if tg_op = 'INSERT' then
    insert into public.violation_status_history(violation_id, from_status, to_status, changed_by, note)
    values (new.id, null, new.status, new.reported_by, 'initial creation');
    return new;
  end if;

  if new.status is distinct from old.status then
    insert into public.violation_status_history(violation_id, from_status, to_status, changed_by, note)
    values (
      new.id,
      old.status,
      new.status,
      coalesce(new.reviewed_by, old.reviewed_by, new.reported_by),
      new.review_note
    );
  end if;
  return new;
end;
$$;

